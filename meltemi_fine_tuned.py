# Checkpoint
def checkpoint(checkpoint_path):
  # Load previous results from checkpoint if it exists
  if os.path.exists(checkpoint_path):
    results_df = pd.read_csv(checkpoint_path)
    processed_reviews = set(results_df["original_review"])
    print(f"Resuming from checkpoint. Already processed: {len(processed_reviews)} reviews.")
  else:
    results_df = pd.DataFrame(columns=["original_review", "model_output"])
    processed_reviews = set()
  return results_df, processed_reviews

# Generation
def generate(prompt, max_new_tokens=200):
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
            eos_token_id=tokenizer.eos_token_id,
        )

    generated_tokens = outputs[0][inputs["input_ids"].shape[1]:]
    answer = tokenizer.decode(generated_tokens, skip_special_tokens=True).strip()
    return answer

# Prediction
def predict(test_df, checkpoint_path, final_output_path, processed_reviews, results_df, task):
  results = []

  # Iterate through the test dataset with progress bar
  for i, row in tqdm(test_df.iterrows(), total=len(test_df)):
    review = row["translation"]

    # Skip if this review has already been processed in a previous run (checkpointing)
    if review in processed_reviews:
      continue

    prompt = prompt_template(review, task)


    try:
      # Run the fine-tuned Meltemi model with the generated prompt
      answer = generate(prompt, max_new_tokens=200)
    # If an error occurs during generation, store the error message instead
    except Exception as e:
        answer = f"Error: {e}"

    # Store the review and model's response in dictionary format
    results.append({
        "original_review": review,
        "model_output": answer
    })

    # Append the new results to the main DataFrame
    results_df = pd.concat([results_df, pd.DataFrame(results)], ignore_index=True)
    results = []

    # Save a checkpoint every 10 samples to prevent data loss
    if i % 10 == 0:
        results_df.to_csv(checkpoint_path, index=False)

  # Final save after the loop ends
  results_df.to_csv(checkpoint_path, index=False)
  results_df.to_csv(final_output_path, index=False)

