# Merge
def merge(pred_df, test_df):
  # Merge on original_review to align predictions with true labels
  merged_df = pd.merge(pred_df, test_df, left_on="original_review", right_on="translation")
  return merged_df

# Sentiment extraction
def extract_sentiment(output, task):
    if not isinstance(output, str):
        output = str(output)

    # Clean the output
    text = output.lower().strip()

    # Remove accents for more robust matching
    text = unicodedata.normalize('NFD', text)
    text = ''.join(c for c in text if unicodedata.category(c) != 'Mn')

    # For structured outputs, look for patterns
    if 'συναισθημα:' in text:
        sentiment_line = text.split('συναισθημα:')[1].split('\n')[0].strip()
        if any(word in sentiment_line for word in ['θετικ', 'positive']):
            return "positive"
        elif any(word in sentiment_line for word in ['αρνητικ', 'negative']):
            return "negative"

    # Fallback to general pattern matching
    if 'θετικ' in text:
        return "positive"
    elif 'αρνητικ' in text:
        return "negative"
    else:
        return "unknown"

# Evaluation
def evaluate(merged_df, path, task):
    # Apply extraction
    merged_df["predicted_sentiment"] = merged_df["model_output"].apply(lambda x: extract_sentiment(x, task))

    # Calculate basic metrics
    merged_df["correct"] = merged_df["predicted_sentiment"] == merged_df["sentiment"]
    accuracy = merged_df["correct"].mean()

    # Count unknowns
    unknown_count = (merged_df["predicted_sentiment"] == "unknown").sum()
    unknown_rate = unknown_count / len(merged_df)

    print(f"Task: {task}")
    print(f"Accuracy: {accuracy * 100:.2f}%")
    print(f"Unknown predictions: {unknown_count}/{len(merged_df)} ({unknown_rate * 100:.2f}%)")

    # Detailed analysis excluding unknowns
    mask = merged_df["predicted_sentiment"] != "unknown"
    if mask.sum() > 0:
        # True labels
        y_true = merged_df.loc[mask, "sentiment"]
        # Predicted labels
        y_pred = merged_df.loc[mask, "predicted_sentiment"]

        # Calculate metrics for both classes
        precision_pos = precision_score(y_true, y_pred, pos_label="positive", average="binary")
        recall_pos = recall_score(y_true, y_pred, pos_label="positive", average="binary")
        f1_pos = f1_score(y_true, y_pred, pos_label="positive", average="binary")

        precision_neg = precision_score(y_true, y_pred, pos_label="negative", average="binary")
        recall_neg = recall_score(y_true, y_pred, pos_label="negative", average="binary")
        f1_neg = f1_score(y_true, y_pred, pos_label="negative", average="binary")

        print(f"Positive class - Precision: {precision_pos:.3f}, Recall: {recall_pos:.3f}, F1: {f1_pos:.3f}")
        print(f"Negative class - Precision: {precision_neg:.3f}, Recall: {recall_neg:.3f}, F1: {f1_neg:.3f}")

        # Macro averages
        macro_f1 = f1_score(y_true, y_pred, average="macro")
        print(f"Macro F1: {macro_f1:.3f}")

    # Save results
    merged_df.to_csv(path, index=False)
    print(f"Results saved to: {path}\n")

# Explanation analysis
def analyze_explanation(merged_df, task):
  if 'explanation' not in task:
    return

  explanations = []
  for output in merged_df["model_output"]:
    if isinstance(output, str) and 'εξήγηση:' in output.lower():
      explanation = output.lower().split('εξήγηση:')[1].split('\n')[0].strip()
      explanations.append(len(explanation.split()))
    else:
      explanations.append(0)

  merged_df["explanation_length"] = explanations
  # Calculate the average word count
  avg_length = sum(explanations) / len(explanations) if explanations else 0
  print(f"Average explanation length: {avg_length:.1f} words")

# Experimentation
def run_experiment(task):
    print(f"\n{'='*50}")
    print(f"Running task: {task}")
    print('='*50)

    checkpoint_path, final_output_path = file_path(task)

    results_df, processed_reviews = checkpoint(checkpoint_path)

    predict(
        test_df=test_df,
        checkpoint_path=checkpoint_path,
        final_output_path=final_output_path,
        processed_reviews=processed_reviews,
        results_df=results_df,
        task=task
    )

    pred_df = output(task)

    merged_df = merge(pred_df, test_df)

    eval_path = f"/content/drive/MyDrive/MSc Thesis/Test/{run_name}_{task}_eval.csv"

    evaluate(merged_df, eval_path, task)

    # Add explanation analysis for relevant tasks
    if 'explanation' in task:
        analyze_explanation(merged_df, task)

    return merged_df


