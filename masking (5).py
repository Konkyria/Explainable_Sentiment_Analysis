# Import required libraries
import pandas as pd
import os
import spacy
from tqdm import tqdm
from llama_cpp import Llama
import random
random.seed(42)
import unicodedata

# Load the quantized Meltemi model (.gguf) using llama-cpp-python
meltemi_llm = Llama(
    model_path="/content/drive/MyDrive/MSc Thesis/meltemi.gguf",
    n_ctx=8192,
    n_threads=8,
    n_batch=256,
    verbose=False
)

# Load the quantized Llama-Krikri model (.gguf) using llama-cpp-python
llama_krikri_llm = Llama(
    model_path="/content/drive/MyDrive/MSc Thesis/llama-krikri.gguf",
    n_ctx=8192,
    n_threads=8,
    n_batch=256,
    verbose=False
)

# Define the best-performing shot per model (only including the explanation_then_sentiment variant)
variants = ["meltemi_16_shot_explanation_then_sentiment", "llama_krikri_6_shot_explanation_then_sentiment"]

all_dfs = []

# Extract only the explanation part from the model_output
def extract_explanation(text):
    explanation = ""
    lines = text.split("\n")
    for line in lines:
        if line.strip().startswith("Εξήγηση:"):
            explanation = line.replace("Εξήγηση:", "").strip()
    return explanation

# Load and process each variant
for variant in variants:
    try:
        path = f"/content/drive/MyDrive/MSc Thesis/Test/{variant}_final.csv"
        df = pd.read_csv(path)

        # Extract only the explanation part from the model_output
        df["original_explanation"] = df["model_output"].apply(extract_explanation)

        # Keep only necessary columns and prepare the final format
        df_final = df[["greek_review", "original_explanation"]].copy()
        df_final["variant"] = variant

        all_dfs.append(df_final)

    except Exception as e:
        print(f"Error processing {variant}: {e}")

# Combine both variants into one dataframe
final_df = pd.concat(all_dfs, ignore_index=True)

# Load Greek spaCy model
nlp = spacy.load("el_core_news_sm")

# Mask 50% of eligible ADJ, ADV, NOUN, VERB tokens within each explanation
def mask_selected_pos(text, mask_ratio=0.5):
    doc = nlp(text)
    selected_pos = {"ADJ", "ADV", "NOUN", "VERB"}

    # Select eligible token indices
    indices = [i for i, token in enumerate(doc) if token.pos_ in selected_pos]

    # Randomly select a subset to mask
    num_to_mask = int(len(indices) * mask_ratio)
    mask_indices = set(random.sample(indices, num_to_mask)) if num_to_mask > 0 else set()

    # Create masked version
    masked_tokens = [("[MASK]" + token.whitespace_) if i in mask_indices else token.text_with_ws for i, token in enumerate(doc)]

    return "".join(masked_tokens)

# Apply masking
final_df["masked_explanation"] = final_df["original_explanation"].apply(mask_selected_pos)

# Split by variant
meltemi_df = final_df[final_df["variant"] == "meltemi_16_shot_explanation_then_sentiment"].copy()
llama_krikri_df = final_df[final_df["variant"] == "llama_krikri_6_shot_explanation_then_sentiment"].copy()

