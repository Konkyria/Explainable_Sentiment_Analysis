# Prompt template
def prompt_template(explanation):
    base_instruction = (
        "Είσαι ένας ειδικός αναλυτής συναισθημάτων για κριτικές ταινιών.\n"
        "Ανάλυσε προσεκτικά την παρακάτω εξήγηση και καθόρισε ξεκάθαρα αν εκφράζει θετικό ή αρνητικό συναίσθημα.\n"
        f"Εξήγηση: «{explanation}»\n"
        "Με βάση την εξήγηση, απάντησε μόνο με μία από τις δύο λέξεις (θετικό ή αρνητικό) με την ακόλουθη μορφή:\n"
        "Συναίσθημα: [θετικό ή αρνητικό]\n"
        "Μην προσθέσεις καμία άλλη λέξη ή εξήγηση.\n"
        "Απάντηση:\n"
    )
    return base_instruction

# Prediction
def predict(csv_path, llm, input_column="masked_explanation", output_column="masked_sentiment"):
    # Load the specific CSV
    if os.path.exists(csv_path):
        print(f"Loading file: {csv_path}")
        df = pd.read_csv(csv_path)
    else:
        raise FileNotFoundError(f"{csv_path} not found.")

    # Add empty output_column, if missing
    if output_column not in df.columns:
        df[output_column] = ""

    # Iterate through the dataset with progress bar
    for i, row in tqdm(df.iterrows(), total=len(df)):
        # Skip, if already predicted
        if isinstance(row[output_column], str) and row[output_column].strip():
            continue

        explanation = row[input_column]
        prompt = prompt_template(explanation)

        try:
            # Run the model with the generated prompt
            output = llm(
                prompt,
                max_tokens=200,
                temperature=0.1,
                top_p=0.9,
                seed=42,
            )

            # Extract the model's answer text from the output structure
            answer = output["choices"][0]["text"].strip()

        # If an error occurs during generation, store the error message instead
        except Exception as e:
            answer = f"Error: {e}"

        df.at[i, output_column] = answer

        # Save a checkpoint every 10 samples to prevent data loss
        if (i + 1) % 10 == 0:
            df.to_csv(csv_path, index=False)
            print(f"Checkpoint saved at index {i + 1}")

    # Final save after the loop ends
    df.to_csv(csv_path, index=False)
    print(f"Final predictions saved to: {output_column} in {csv_path}")

    return df

# Convert Greek sentiment labels to English equivalents
def normalize_sentiment(text):
    if not isinstance(text, str):
        text = str(text)

    # Clean the text
    sentiment = text.lower().strip()

    # Remove accents for more robust matching
    sentiment = unicodedata.normalize('NFD', sentiment)
    sentiment = ''.join(c for c in sentiment if unicodedata.category(c) != 'Mn')

    if 'θετικ' in sentiment:
        return "positive"
    elif 'αρνητικ' in sentiment:
        return "negative"
    else:
        return "unknown"

# Evaluation
def evaluate(df, csv_path):
    # Convert Greek sentiment labels to English equivalents
    df["original_sentiment_normalized"] = df["original_sentiment"].apply(normalize_sentiment)
    df["masked_sentiment_normalized"] = df["masked_sentiment"].apply(normalize_sentiment)

    # Compute flip rate
    df["sentiment_changed"] = df["original_sentiment_normalized"] != df["masked_sentiment_normalized"]
    flip_rate = df["sentiment_changed"].mean()
    print(f"Agreement: {(1-flip_rate)*100:.2f}%  ---  Flip rate: {flip_rate*100:.2f}%")

    # Save updated CSV
    df.to_csv(csv_path, index=False)
    print(f"Results saved to: {csv_path}")

