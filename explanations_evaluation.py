# Prompt template
def create_prompt(row):
    return f"""
Είσαι ένας ειδικός αξιολογητής εξηγήσεων συναισθηματικής ανάλυσης.

Σου δίνεται:
1. Μια κριτική ταινίας στα Ελληνικά
2. Μια σωστή εξήγηση
3. Μια εξήγηση από ένα μοντέλο

Αξιολόγησε την εξήγηση του μοντέλου σε σύγκριση με την σωστή εξήγηση, με βάση τα τέσσερα ακόλουθα κριτήρια:
1. Ορθότητα: Ταιριάζει η εξήγηση του μοντέλου με το πραγματικό συναίσθημα της κριτικής;
2. Σύγκλιση: Είναι παρόμοια η εξήγηση του μοντέλου με τη σωστή εξήγηση σε περιεχόμενο και αιτιολόγηση;
3. Ροή: Είναι γραμματικά και συντακτικά ορθή η εξήγηση του μοντέλου;
4. Πληρότητα: Εξηγεί πλήρως η εξήγηση του μοντέλου γιατί η κριτική είναι θετική ή αρνητική;

Βαθμολόγησε το κάθε κριτήριο από 1 έως 5.
Υπολόγισε και το άθροισμα (μέγιστο 20).

--- Κριτική ταινίας ---
«{row['review']}»

--- Σωστή Εξήγηση ---
{row['gold_explanation']}

--- Εξήγηση Μοντέλου ---
{row['model_explanation']}

Απάντησε με την εξής μορφή:
Ορθότητα: X
Σύγκλιση: X
Ροή: X
Πληρότητα: X
Άθροισμα: XX
"""

# Load checkpoint, if it exists, to resume scoring progress
checkpoint_path = "/content/drive/MyDrive/MSc Thesis/scored_explanations_checkpoint.csv"
if os.path.exists(checkpoint_path):
    print("Loading checkpoint...")
    df = pd.read_csv(checkpoint_path)
else:
    df = merged_df.copy()

# Ensure scoring columns exist in the dataframe (add them, if missing)
for col in ["correctness", "alignment", "fluency", "completeness", "total"]:
    if col not in df.columns:
        df[col] = 0

# Initialize the Google Gemini LLM for explanation evaluation
model = genai.GenerativeModel("gemini-2.5-pro")

print("Scoring explanations with rate-limiting and checkpointing...")

# Iterate through each row of the dataframe
for idx, row in tqdm(df.iterrows(), total=len(df)):

    # Skip rows that have already been scored
    if df.at[idx, "total"] > 0:
        continue

    # Create the evaluation prompt from the current row
    prompt = create_prompt(row)

    try:
        # Generate evaluation from the Google Gemini LLM
        response = model.generate_content(prompt)
        content = response.text
        lines = content.strip().split('\n')

        # Parse the response and extract individual scores
        for line in lines:
            if "Ορθότητα" in line:
                df.at[idx, 'correctness'] = int(line.split(":")[1].strip())
            elif "Σύγκλιση" in line:
                df.at[idx, 'alignment'] = int(line.split(":")[1].strip())
            elif "Ροή" in line:
                df.at[idx, 'fluency'] = int(line.split(":")[1].strip())
            elif "Πληρότητα" in line:
                df.at[idx, 'completeness'] = int(line.split(":")[1].strip())
            elif "Άθροισμα" in line:
                df.at[idx, 'total'] = int(line.split(":")[1].strip())

    except Exception as e:
        print(f"Error on row {idx}: {e}")
        df.at[idx, 'total'] = -1

    # Save checkpoint after every evaluation to preserve scoring progress
    df.to_csv(checkpoint_path, index=False)

    # Respect Google Gemini API rate limits (2 requests per minute for free tier)
    if (idx + 1) % 2 == 0:
        print("Sleeping 60 seconds to respect Google Gemini API rate limits...")
        time.sleep(60)

